// Exemplo de declaração de variáveis

// Utilizando var
var nome = "Igor";
var idade = 26;

console.log(nome);
console.log(idade);

// Utilizando let
let quantidade = 10;
let preco = 19.99;

console.log(`\n${quantidade}`);
console.log(preco);

// Utilizando const
const pi = 3.14159;
const url = "https://www.google.com";

console.log(`\n${pi}`);
console.log(url);

