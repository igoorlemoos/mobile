// Operador Atribuição

// Atribuição Simples
let x = 10;
console.log("Atribuição simples: " + x);

// Atribuição de adição
let y = 5
y += 3;
console.log("Atribuição de adição: " + y);

// Atribuição de subtração
let z = 20;
z -= 8;
console.log("Atribuição de subtração: " + z);

// Atribuição de divisão
let b = 100;
b /= 4;
console.log("Atribuição de divisão: " + b);

// Atribuição de multiplicação
let m = 6;
 m *= 5;
console.log("Atribuição de multiplicação: " + m);