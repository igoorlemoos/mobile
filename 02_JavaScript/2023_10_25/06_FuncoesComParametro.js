// Funções com parametros que retorna a soma de dois numeros
function somar(a, b) {
    return a + b;
}

// Chamada da função com diferente valores para os parametros
const resultado1 = somar(5, 3);
const resultado2 = somar(10, -2);
const resultado3 = somar(2.5, 1.5);

// Exibição dos resultados
console.log(`Resultado 1: ${resultado1}`);
console.log("Resultado 2: " + resultado2);
console.log("Resultado 3: " + resultado3);